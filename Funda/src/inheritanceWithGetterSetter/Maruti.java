package inheritanceWithGetterSetter;

public class Maruti {
	
	int price;
	String color,name;
	 public void setName(String name)
	 {
		 this.name=name;
	 }
	 public String getName() {
		 return name;
	 }
	 
	 public void setPrice(int price) {
		 this.price=price;
	 }
	 
	 public int getPrice() {
		 return price;
	 }
	 public void setColor(String color) {
		 this.color=color;
	 }
	 public String getColor() {
		 return color;
	 }

}
